class Calculator:

    first_number = 0
    second_number = 0

    
    def sum(self):
        return self.first_number + self.second_number

    
    def sub(self):
        return self.first_number - self.second_number


    def multiply(self):
        return self.first_number * self.second_number

    
    def divide(self):
        return self.first_number / self.second_number
